// С помощью цикла while вывести все простые числа в промежутке от 0 до 100.
"use strict"

let a = 2;
simp();

function simp(){
    while (a < 100){
        if ((a === 2 || a === 3 || a === 5 || a === 7) || (a % 2 !== 0 && a % 3 !== 0 && a % 5 !== 0 && a % 7 !== 0)){
            console.log(a);
        }
        a++;
    }
}